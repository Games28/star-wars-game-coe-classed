Game::Game(MainWindow& wnd)
	:
	wnd(wnd),
	gfx(wnd)
{
	
	std::random_device rd;
	std::mt19937 rng(rd());
	std::uniform_int_distribution<int> xdist(0, 770);
	std::uniform_int_distribution<int> ydist(0, 570);
	tie0.x = xdist(rng);
	tie0.y = ydist(rng);
	tie1.x = xdist(rng);
	tie1.y = ydist(rng);
	tie2.x = xdist(rng);
	tie2.y = ydist(rng);
	tie3.x = xdist(rng);
	tie3.y = ydist(rng);

}

void Game::Go()
{
	gfx.BeginFrame();
	UpdateModel();
	ComposeFrame();
	gfx.EndFrame();
}


void Game::UpdateModel()
{
	if (wnd.kbd.KeyIsPressed(VK_UP))
	{
		wing.y -= 1;
	}
	if (wnd.kbd.KeyIsPressed(VK_DOWN))
	{
		wing.y += 1;
	}
	if (wnd.kbd.KeyIsPressed(VK_LEFT))
	{
		wing.x -= 1;
	}
	if (wnd.kbd.KeyIsPressed(VK_RIGHT))
	{
		wing.x +=  1;
	}

	wing.boundry();

	tie0.Isdestroyed(wing.x, wing.y, wing.width, wing.height);
	tie1.Isdestroyed(wing.x, wing.y, wing.width, wing.height);
	tie2.Isdestroyed(wing.x, wing.y, wing.width, wing.height);
	tie3.Isdestroyed(wing.x, wing.y, wing.width, wing.height);
}

